    private int getDaysInMonth(int year, int month) {
        switch (month) {
            case 1, 3, 5, 7, 8, 10, 12 -> {
                return 31;
            }
            case 4, 6, 9, 11 -> {
                return 30;
            }
            case 2 -> {
                if ((year % 4 == 0 && year % 100 != 0) || (year % 400 == 0)) {
                    return 29;
                }
                return 28;
            }
        }
        return 0;
    }

    @Override
    public void addNotify() {
        super.addNotify();
        LocalDate date = DayManager.getDays().get(0).date();
        int year = date.getYear();
        int month = date.getMonthValue();
        int daysInMonth = getDaysInMonth(year, month);

        lblMonth.setText(month + " / " + year);
        if (daysInMonth < 31) {
            chk31.setEnabled(false);
        }
        if (daysInMonth < 30) {
            chk30.setEnabled(false);
        }
        if (daysInMonth < 29) {
            chk29.setEnabled(false);
        }

        List<Day> daysOff = doctor.daysOff();
        for (Day day : daysOff) {
            arrChk[day.dayID() - 1].setSelected(true);
        }
    }

