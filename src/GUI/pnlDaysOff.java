    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        List<Integer> daysOff = new ArrayList<>();

        for (int i = 0; i < arrChk.length; i++) {
            if (arrChk[i].isSelected()) {
                daysOff.add(i + 1);
            }
        }

        doctor.daysOff().clear();
        doctor.daysOff().addAll(DayManager.getDays().stream()
                .filter(day -> daysOff.contains(day.dayID()))
                .collect(Collectors.toList()));

        DoctorManager.setDoctors(DoctorManager.getDoctors());
    }//GEN-LAST:event_btnSaveActionPerformed


